const KEYS = Object.keys(COLOR_HEX).sort((a, b) => b.length - a.length);
const strip = (s: string) => s.normalize('NFKD').replace(/[̀-ͯ]/g, '').toLowerCase();

// Retourne le hex de la 1re couleur reconnue dans le libelle d attribut, sinon undefined.
export function colorFor(attr: string): string | undefined {
  if (!attr) return undefined;
  const a = strip(attr);
  if (COLOR_HEX[a]) return COLOR_HEX[a];
  for (const k of KEYS) { if (a.includes(k)) return COLOR_HEX[k]; }
  return undefined;
}